package com.hbcsoft.workflow.entity;

import javax.persistence.Table;

@Table(name="HBC_F_NODERULE")
public class WorkflowNodeRule extends WorkflowBase {
	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = 1L;
	
	/**
	 * 流程ID
	 */
	private long wfId;
	
	/**
	 * 节点ID
	 */
	private long nodeId;
	
	/**
	 * 实体标题
	 */
	private String tableName;
	
	/**
	 * 实体名称
	 */
	private String tableId;
	
	/**
	 * 字段标题
	 */
	private String columnName;
	
	/**
	 * 字段名称
	 */
	private String columnId;
	
	/**
	 * 规则类型
	 */
	private String ruleType;
	
	/**
	 * 规则值
	 */
	private String ruleValue;
	
	/**
	 * 跳转节点ID
	 */
	private long toNodeId;
	
	/**
	 * 规则名称
	 */
	private String ruleName = "";
	
	/**
	 * 规则序号
	 */ 
	private Integer ruleSort;
	
	
	public Integer getRuleSort() {
		return ruleSort;
	}
	public void setRuleSort(Integer ruleSort) {
		this.ruleSort = ruleSort;
	}

	public String getRuleName() {
		return ruleName;
	}
	public void setRuleName(String ruleName) {
		this.ruleName = ruleName;
	}

	public long getWfId() {
		return wfId;
	}
	public void setWfId(final long wfId) {
		this.wfId = wfId;
	}

	public long getNodeId() {
		return nodeId;
	}
	public void setNodeId(final long nodeId) {
		this.nodeId = nodeId;
	}

	public String getTableName() {
		return tableName;
	}
	public void setTableName(final String tableName) {
		this.tableName = tableName;
	}

	public String getTableId() {
		return tableId;
	}
	public void setTableId(final String tableId) {
		this.tableId = tableId;
	}

	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(final String columnName) {
		this.columnName = columnName;
	}

	public String getColumnId() {
		return columnId;
	}
	public void setColumnId(final String columnId) {
		this.columnId = columnId;
	}

	public String getRuleType() {
		return ruleType;
	}
	public void setRuleType(final String ruleType) {
		this.ruleType = ruleType;
	}

	public String getRuleValue() {
		return ruleValue;
	}
	public void setRuleValue(final String ruleValue) {
		this.ruleValue = ruleValue;
	}

	public long getToNodeId() {
		return toNodeId;
	}
	public void setToNodeId(final long toNodeId) {
		this.toNodeId = toNodeId;
	}
}
