package com.hbcsoft.tag;

/**
 * 存储事件及对应的方法名
 * @author liang
 *
 */
public class EventCollection {

	/**
	 * 事件名称
	 */
	private String eventName;
	/**
	 * 事件
	 */
	private String eventFun;
	/**
	 * 无参构造方法
	 */
	public EventCollection(){
		// This constructor is intentionally empty. Nothing special is needed here.
	}
	/**
	 * 有参构造方法
	 * @param eventName
	 * @param eventFun
	 */
	public EventCollection(final String eventName,final String eventFun){
		this.eventName = eventName;
		this.eventFun = eventFun;
	}
	/**
	 * 事件名称
	 * @return
	 */
	public String getEventName() {
		return eventName;
	}
	/**
	 * 设置事件名称
	 * @param eventName
	 */
	public void setEventName(final String eventName) {
		this.eventName = eventName;
	}
	/**
	 * 事件
	 * @return
	 */
	public String getEventFun() {
		return eventFun;
	}
	/**
	 * 设置事件
	 * @param eventFun
	 */
	public void setEventFun(final String eventFun) {
		this.eventFun = eventFun;
	}
	
	
}
